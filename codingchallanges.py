length = int(input("enter a area of a cuboid: "))
breadth = int(input("enter a breadth of a cuboid: "))
height = int(input("enter a height of a cuboid: "))

area = 2 * (length * height + length * breadth + breadth * height)

print("the area of the cuboid is ",area)

totalanswer = area

